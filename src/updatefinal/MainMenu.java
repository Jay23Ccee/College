   public String[] BuildDaysBox(String Month)
   {
     String[] Days = null;
     
     if(Month.equalsIgnoreCase("Jan")|| Month.equalsIgnoreCase("Mar")|| Month.equalsIgnoreCase("May")||Month.equalsIgnoreCase("Jul")||Month.equalsIgnoreCase("Aug")||Month.equalsIgnoreCase("Oct")||Month.equalsIgnoreCase("Dec")){
         Days = new String[31];
         for(int x=0; x<31; x++){
             
             Days[x]=Integer.toString(x+1);
             
         }
         
         return Days;
         
     }
     else if(Month.equalsIgnoreCase("Apr")||Month.equalsIgnoreCase("Jun")||Month.equalsIgnoreCase("Sept")||Month.equalsIgnoreCase("Nov"))
     {
         
         Days= new String[30];
           for(int x=0; x<30; x++){
             
             Days[x]=Integer.toString(x+1);
             
         }
         
         
         return Days;
     }
     else if (Month.equalsIgnoreCase("Feb"))
     {
         Days= new String[28];
         for( int x=0; x<28; x++)
         {
             Days[x] = Integer.toString(x+1);
             
         }
         
     }
    return Days;   
   }
   
   
   
   
   
   
   
   
